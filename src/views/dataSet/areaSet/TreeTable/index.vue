<template>
  <el-table
    ref="treeTable"
    :data="formatData"
    :row-style="showRow"
    v-bind="$attrs"
    highlight-current-row
    @current-change="handleCurrentChange">
    <el-table-column v-if="columns.length===0" width="150">
      <template slot-scope="scope">
        <span v-for="space in scope.row._level" :key="space" class="ms-tree-space"/>
        <span v-if="iconShow(0,scope.row)" class="tree-ctrl" @click="toggleExpanded(scope.$index)">
          <i v-if="!scope.row._expanded" class="el-icon-plus"/>
          <i v-else class="el-icon-minus"/>
        </span>
        {{ scope.$index }}
      </template>
    </el-table-column>
    <el-table-column v-for="(column, index) in columns" v-else :key="column.value" :label="column.text" :width="column.width">
      <template slot-scope="scope">
        <!-- Todo -->
        <!-- eslint-disable-next-line vue/no-confusing-v-for-v-if -->
        <span v-for="space in scope.row._level" v-if="index === 0" :key="space" class="ms-tree-space"/>
        <span v-if="iconShow(index,scope.row)" class="tree-ctrl" @click="toggleExpanded(scope.$index,scope.row)">
          <i v-if="!scope.row._expanded" class="el-icon-plus"/>
          <i v-else class="el-icon-minus"/>
        </span>
        {{ scope.row[column.value] }}
      </template>
    </el-table-column>
    <slot/>
  </el-table>
</template>

<script>
/**
  Auth: Lei.j1ang
  Created: 2018/1/19-13:59
*/
import { isFwRegionOnly, loadRegionInfo, getFwRegion, getFwRegionLower, getFwRegionLowerAll, getFwRegionQuery, addFwRegionOn, deleteFwRegionById, updateFwRegion } from '@/api/dataSet/areaSet'

import treeToArray from './eval'
export default {
  name: 'TreeTable',
  props: {
    /* eslint-disable */
    data: {
      type: [Array, Object],
      required: true
    },
    columns: {
      type: Array,
      default: () => []
    },
    evalFunc: Function,
    evalArgs: Array,
    expandAll: {
      type: Boolean,
      default: false
    }
  },
  computed: {
    // 格式化数据源
    formatData: function() {
      let tmp
      if (!Array.isArray(this.data)) {
        tmp = [this.data]
      } else {
        tmp = this.data
      }
      const func = this.evalFunc || treeToArray
      const args = this.evalArgs ? Array.concat([tmp, this.expandAll], this.evalArgs) : [tmp, this.expandAll]
      return func.apply(null, args)
    }
  },
  methods: {
    showRow: function(row) {
      const show = (row.row.parent ? (row.row.parent._expanded && row.row.parent._show) : true)
      row.row._show = show
      return show ? 'animation:treeTableShow 1s;-webkit-animation:treeTableShow 1s;' : 'display:none;'
    },
    handleCurrentChange(val) {
      this.$emit('getselecttd',val);
    },
    // 切换下级是否展开
    toggleExpanded: function(trIndex,row) {
      if(row.REGION_LOWER_SIZE>=row.children.length){
        getFwRegionLower({regionId: row.REGION_ID}).then(response => {
          response.data.forEach((a, b) => {
            if (a.children === undefined) {
              a.children = []
            }
          })
          this.resetData(response.data)
          row.children = response.data
          const record = this.formatData[trIndex]
          record._expanded = !record._expanded
        })
      }else{
        const record = this.formatData[trIndex]
        record._expanded = !record._expanded
      }
    },
    resetData(data) {
      data.forEach((a, b) => {
        var typeName = ['暂无', '国家', '省份', '城市', '县区', '街道']
        a.REGION_TYPE_NAME = typeName[a.REGION_TYPE]
      })
    },
    // 图标显示
    iconShow(index, record) {
      return (index === 0 && record.REGION_LOWER_SIZE )
    }
  }
}
</script>
